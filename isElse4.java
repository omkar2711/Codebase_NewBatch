public class isElse4 {
    public static void main(String[] args) {
        int num = 35;

        if(num % 5 == 0){
            System.out.println("Number is divisible by 5");
        }
        if(num % 7 == 0){
            System.out.println("Number is divisible by 7");
        }
        else{
            System.out.println("Number is not divisible by 5 or 7");
        }
    }
}
