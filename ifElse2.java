public class ifElse2 {
    public static void main(String[] args) {
        String light = "green";

        if(light == "red"){
            System.out.println("Stop!");
        }
        else if(light == "yellow"){
            System.out.println("Slow Down!");
        }
        else if(light == "green"){
            System.out.println("Go!");
        }
    }
}
